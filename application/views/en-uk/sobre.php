		<h2>History</h2>

		<p>The Laboratory of Freshwater Ecology was created in 2006 in the Department of Ecology and Zoology of the Center for Biological Sciences, Federal University of Santa Catarina. Since then, is developing studies and projects in Freshwater Ecology / Limnology area. It also aims at training qualified personnel and consolidation of a research group / laboratories from the Ecology and Plant Biology Post Graduate Program (Center for Biological Sciences) and Aquaculture Post Graduate Program (Center for Agricultural Sciences) from Federal University of Santa Catarina.</p>
		<p>During 2006 the laboratory received funding from the University to develop two researches projects in the region. In 2008 began a project in Peri coastal lagoon (Supported by CNPq) with the participation of researchers from Ecology and Zoology, and Aquaculture departments. This project made possible the first two master dissertations from the Ecology Post Graduate Program concluded in 2010. Also in 2008, was beginning a master dissertation from the Plant Biology Post Graduate Program, aimed on the leaf breakdown process of native mangroves species.</p>
		<p>In 2009, also in Peri coastal lagoon was implemented a research project that aimed to estimate phytoplankton primary production rates. By 2009, three graduation monographs have already been designed, developed and finalized in the laboratory and there are new opportunities to be developed.</p>
		<p>The main objective of the research laboratory is to contribute with attitudes, concepts, techniques and methodologies to develop discussions, which incorporated the qualification, to train professionals to work in the environmental area, especially those related to water resources.</p>

