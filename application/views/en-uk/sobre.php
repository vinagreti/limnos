		<h1 class="text-center">Sobre</h1>

		<br></br>

		<h2>Histórico</h2>

		<p>O Laboratório de Ecologia de Águas Continentais foi criado em 2006 no Departamento de Ecologia e Zoologia, do Centro de Ciências Biológicas da Universidade Federal de Santa Catarina. Desde então, vem atuando em estudos e projetos na área de Ecologia de Águas Doces/Limnologia. Visa realizar estudos e novas propostas de trabalhos nas lagoas, áreas alagadas, rios e reservatórios da região. Objetiva também a formação de pessoal qualificado e a consolidação de um grupo de pesquisadores/laboratórios associados aos programas de Pós Graduação em Ecologia, Biologia Vegetal e Aquicultura dos Centros de Ciências Biológicas e de Ciências Agrárias da UFSC.
		<p>Durante o ano de 2006 o laboratório foi contemplado com um projeto Funpesquisa (Fonte de Financiamento: UFSC) e um projeto FUNGRAD (Fonte de Financiamento: UFSC). Em 2008 iniciou-se um projeto na lagoa do Peri (Edital Universal CNPq) com a participação de pesquisadores dos departamentos de Ecologia e Zoologia e de Aquicultura (LAPAD). Este projeto viabilizou as duas primeiras dissertações de mestrado do Programa de Pós Graduação em Ecologia da UFSC, concluídas em 2010. Ainda em 2008, deu-se inicio um projeto de dissertação de mestrado do Programa de Pós Graduação em Biologia Vegetal da UFSC, sobre a decomposição de detritos vegetais em pequenos rios dos manguezais do Itacurubi e Carijós.</p>
		<p>No ano de 2009, também na lagoa do Peri, foi implementado um projeto que visou estimar taxas de produção primária de algas. Até 2009, três Trabalhos de Conclusão de Curso – TCC, já foram concebidos, desenvolvidos e finalizados no laboratório e existem oportunidades para novos serem realizados.</p>
		<p>O objetivo principal da pesquisa dos laboratórios é contribuir com atitudes, conceitos, técnicas e metodologias, para desenvolver debates que, incorporados à qualificação profissional, procurem formar profissionais para atuarem na área ambiental, especialmente aquelas relativas aos recursos hídricos.</p></p>

		<hr>

		<h2>Missão</h2>

		<p>Proporcionar atividades de pesquisa, extensão e ensino na área de ecologia aquática e que possam atender não apenas ao meio acadêmico, mas também a comunidade (gerentes ambientais, tomadores de decisão e a população de maneira geral)</p>

		<hr>

		<h2>Visão</h2>

		<p>Formação de pesquisadores e profissionais críticos e éticos que possam desenvolver trabalhos na área de ecologia de águas continentais.</p>

		<hr>

		<h2>Valores</h2>

		<p>Incluir na vida acadêmica dos universitários o universo da pesquisa, extensão e do ensino como complemento das suas atividades de graduação. Levantamento de informações e o conhecimento dos recursos hídricos da região de Santa Catarina.</p>

		<hr>

		<h2>Produtos/Serviços</h2>

		<p>Produção de conhecimento, formação de corpo técnico e profissionais especializados (Graduação e Pós-Graduando), Atender as demandas locais e regionais na área de ecologia de águas continentais.</p>

		<hr>

		<h2>Diferencial</h2>

		<p>Pesquisa e levantamento de informações; formação de profissionais especializados.</p>

		<hr>

		<h2>Público interno</h2>

		<p>Alunos de graduação e pós-graduação, professores e colaboradores do Departamento de Ecologia e Zoologia do Centro de Ciências Biológicas (CCB), do Centro de Filosofia e Ciências Humanas (CFH) e do Centro de Ciências Agrárias (CCA).</p>

		<hr>

		<h2>O público externo</h2>

		<p>Alunos de Universidades, alunos de escolas, comunidade local, interessados de outras instituições de pesquisa nacionais e internacionais, Prefeitura Municipal de Florianópolis, Conselho Regional de Biologia, Ministério Público, entre outros. Órgão ambientais (IBAMA, FATMA, FLORAM), gerentes e tomadores de decisão na área de meio ambientes e empresas.

